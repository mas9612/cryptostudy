func TestKeccak(t *testing.T) {
	type data struct {
		d int
		M []byte
	}
	inputs := []data{
		data{0, make([]byte, 0)},
	}
	expected := [][]byte{
		make([]byte, 0),
	}
	for i, input := range inputs {
		hash, err := Keccak(input.d, input.M)
		if err != nil {
			t.Error(err)
		}
		if !bytes.Equal(hash, expected[i]) {
			t.Errorf("[TestKeccak] Case %d failed: result '%x', but expected '%x'\n", i, hash, expected[i])
		}
	}
}

